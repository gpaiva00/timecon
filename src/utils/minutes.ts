export const minutesToMilliseconds = (minutes: number) => {
  return minutes * 60000
}

export const minutesToSeconds = (minutes: number) => {
  return minutes * 60
}

export const minutesToHours = (minutes: number) => {
  return minutes / 60
}

export const minutesToDays = (minutes: number) => {
  return minutes / 1440
}

export const minutesToWeeks = (minutes: number) => {
  return minutes / 10080
}

export const minutesToMonths = (minutes: number) => {
  return minutes / 43830
}

export const minutesToYears = (minutes: number) => {
  return minutes / 525960
}
