export const yearsToMilliseconds = (years: number) => {
  return years * 31536000000
}

export const yearsToSeconds = (years: number) => {
  return years * 31536000
}

export const yearsToMinutes = (years: number) => {
  return years * 525600
}

export const yearsToHours = (years: number) => {
  return years * 8760
}

export const yearsToDays = (years: number) => {
  return years * 365
}

export const yearsToWeeks = (years: number) => {
  return years * 52
}

export const yearsToMonths = (years: number) => {
  return years * 12
}
