export const secondsToMilliseconds = (seconds: number) => {
  return seconds * 1000
}

export const secondsToMinutes = (seconds: number) => {
  return seconds / 60
}

export const secondsToHours = (seconds: number) => {
  return seconds / 3600
}

export const secondsToDays = (seconds: number) => {
  return seconds / 86400
}

export const secondsToWeeks = (seconds: number) => {
  return seconds / 604800
}

export const secondsToMonths = (seconds: number) => {
  return seconds / 2628000
}

export const secondsToYears = (seconds: number) => {
  return seconds / 31536000
}
