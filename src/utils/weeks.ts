export const weeksToMilliseconds = (weeks: number) => {
  return weeks * 604800000
}

export const weeksToSeconds = (weeks: number) => {
  return weeks * 604800
}

export const weeksToMinutes = (weeks: number) => {
  return weeks * 10080
}

export const weeksToHours = (weeks: number) => {
  return weeks * 168
}

export const weeksToDays = (weeks: number) => {
  return weeks * 7
}

export const weeksToMonths = (weeks: number) => {
  return weeks / 4.34812
}

export const weeksToYears = (weeks: number) => {
  return weeks / 52.1429
}
